import fs from "node:fs";
import {createClient} from "@supabase/supabase-js";
const env = Object.fromEntries(fs.readFileSync(".env.local", "utf8").split(/\r?\n/).filter(Boolean).map((line) => { const i = line.indexOf("="); return [line.slice(0, i), line.slice(i + 1)]; }));
const response = await fetch(`https://api.supabase.com/v1/projects/${new URL(env.NEXT_PUBLIC_SUPABASE_URL).hostname.split(".")[0]}/api-keys`, {headers: {Authorization: `Bearer ${env.SUPABASE_ACCESS_TOKEN}`}});
const keys = await response.json();
const service = keys.find((key) => (key.name ?? key.api_key_name) === "service_role").api_key;
const supabase = createClient(env.NEXT_PUBLIC_SUPABASE_URL, service, {auth: {persistSession: false}});
const {data, error} = await supabase.from("skills").select("slug,title,skill_faqs!inner(sort_order)").eq("status", "active").eq("visibility", "public").order("slug");
if (error) throw error;
console.log(JSON.stringify({skills: (data ?? []).length, faqCounts: Object.fromEntries((data ?? []).map((item) => [item.slug, item.skill_faqs.length]))}));
